public class datatypes {
    public static void main(String[] args) {
        // Name - "Saurabh bansal " --> words / sentence 
        // age - 25                 --> Integer
        // gender - m               --> character 
        // fit - true               --> yes/no
        //height - 5.10            --> Decimal 

        // DataType VariableName = VariableValue ;

        String name = "Saurabh Bansal";
        int age = 25 ;
        char gender = 'm';
        boolean fit = true ;
        double height = 5.10;


        System.out.println("name -" + name);
        System.out.println(age);
        System.out.println(gender + " " + fit + "  " + height );


    }
}
